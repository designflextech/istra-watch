"""–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /start"""
from telegram import Update
from telegram.ext import ContextTypes
from bot.config import is_admin
from bot.keyboards.admin_keyboard import get_admin_keyboard
